# Iteration over a List
fruits = ["apple", "banana", "cherry", "date", "fig"]

print("Iterating over a List:")
for fruit in fruits:
    print(f"Fruit: {fruit}")

# Iteration over a Dictionary
person = {
    "name": "John",
    "age": 30,
    "city": "New York",
}

print("\nIterating over a Dictionary:")
for key, value in person.items():
    print(f"{key}: {value}")
